    <h2 class="h2Historia">Historia</h2>
    <pre class="texto" id="texto" >
        El Airbus A300-600ST, conocido popularmente como Beluga fue fabricado por el consorcio Airbus, siendo sucesora del antiguo Super Guppy. El Beluga 
        es un reactor de carga extraordinario, diseñado para realizar vuelos de corta y media distancia. capaz de transportar cargas de gran volumen. Su
        primer vuelo tuvo lugar en 1994, revolucionando la cadena de producción de Airbus con una solución de diseño innovadora. Para crear ese aspecto de 
        "joroba", los ingenieros tomaron como base el A300-600, pero desplazaron la cabina de mando hacia abajo y añadieron una estructura superior que le
        da ese aspecto a una ballena beluga. Su diseño de gran capacidad le permite operar en aeropuertos con pistas no tan largas y también ha realizado 
        misiones especiales transportando desde satélites y helicópteros hasta obras de arte, demostrando que su enorme joroba puede albergar los tesoros
        mas diversos de nuestra era
    </pre>


