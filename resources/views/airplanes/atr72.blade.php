    <h2 class="h2Historia">Historia</h2>
    <pre class="texto" id="texto" >
        El ATR 72 es uno de los aviones turbohelice mas exitosos y reconocibles de la aviación comercial. Lo fabrica el consorcio franco-italiano ATR y forma 
        parte de una familia que comparte con el ATR 42. El "72" en su nombre hace referencia a su capacidad estándar, albergando entre 70 y 78 pasajeros en 
        un diseño de fuselaje estrecho, pensado para operaciones eficientes en rutas de corta y media distancia. Su primer vuelo tuvo lugar en 1988, 
        revolucionando el mercado regional gracias a su economía, ya que sus motores turbohélice consumen mucho menos que un reactor y su capacidad para 
        operar en pistas cortas lo convierte en la herramienta perfecta para regiones aisladas. Una de sus características es que, a diferencia de la mayoría 
        de aviones, los pasajeros suelen embarcar por una puerta trasera, mientras que la delantera se reserva para la carga.  La versión mas moderna, la -600, 
        incorpora cabinas de cristal con pantallas LCD y motores mas eficientes que reducen el consumo y los costos de mantenimiento , convirtiéndolo en la 
        opción preferida de aerolíneas de todo el mundo.
    </pre>

