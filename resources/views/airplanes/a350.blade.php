    <h2 class="h2Historia">Historia</h2>
    <pre class="texto" id="texto" >
        El Airbus A350 es un tipo de avión comercial que se cuenta entre los más modernos de la aviación actual, el cual fue desarrollado por el
        fabricante europeo Airbus, y que se incluye en una nueva hornada de aviones de fuselaje ancho que están concebidos para poder afrontar 
        trayectos y vuelos de larga distancia, esta aeronave abarca un reactor bimotor y bimotores que tiene como misión conseguir consumos más 
        eficientes de combustible, las reducciones de emisiones y, en suma, una absolutamente mayor comodidad para el pasaje a la hora de acometer 
        vuelos de tipo intercontinental. Su primera avión fue, data de 2013, y se puede afirmar que este avión ha dado un gran soplo de aire fresco 
        desde el punto de vista tecnológico mediante el uso de materiales compuestos, por ejemplo, los plásticos, fibrosos de carbono y por la presencia 
        de sistemas digitales de última generación que preparan la naciente navegación para la seguridad, la precisión de volar y la operación del reactor
    </pre>
    <br>
    <pre class="texto">
        Además, este modelo es reconocido por su gran autonomía y capacidad para conectar continentes sin escalas, lo que lo convierte en una
        opción ideal para aerolíneas que operan rutas internacionales de largo alcance. Su diseño moderno permite una cabina más silenciosa,
        ventanas panorámicas de mayor tamaño y un sistema avanzado de presurización que reduce la fatiga en vuelos extensos, desde su entrada
        en servicio comercial en 2015, el A350 se ha consolidado como uno de los aviones más eficientes, innovadores y competitivos dentro del
        mercado global de la aviación. 
    </pre>

