    <h2 class="h2Historia">Historia</h2>
    <pre class="texto" id="texto" >
        El Embraer E195 es uno de los aviones regionales mas exitoso de la aviacion comercial, elaborado por el fabricante brasileño Embraer. 
        Forma parte de la familia E-Jets, que incluye los modelos E170, E175 E190 y E195. El Embraer 195 es un reactor de fuselaje estrecho, 
        diseñado para operar en rutas cortas y de media distancia. Su primer vuelo se realizo en 2004, pero la version mas moderna realizo 
        su primer vuelo en 2017. Este modelo es conocido por su robustez y por los bajos costos de mantenimiento a comparacion de otros aviones, 
        lo que lo convierte en una buena opcion para las aerolineas si buscan conectar ciudades secundarias o para reforzar la frecuencia en 
        rutas de alta demanda evitando el uso de aviones de gran capacidad.  Su diseño compacto y eficiente le permite operar con normalidad 
        en aeropuertos con infraestructura limitada, ampliando las posibilidades de conectividad regional
    </pre>

