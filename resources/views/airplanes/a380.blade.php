    <h2 class="h2Historia">Historia</h2>
    <pre class="texto" id="texto" >
        El Airbus A380 es el mayor avión comercial de pasajeros disponible en el mercado en la actualidad, este esfabricado por el consorcio europeo 
        Airbus con la intención de cambiar el transporte aéreo de gran volumen en rutas internacionales. Este modelo de doble cubierta completa fue
        concebido para responder al aumento creciente del tráfico aéreo mundial, principalmente en los aeropuertos donde hay más congestión. El A380 es un
        reactor de cuatro motores desarrollado para la obra de los vuelos de larga distancia e incorpora una extraordinaria capacidad, estabilidad en
        vuelo y unos niveles de cilindrada y confort para los pasajeros. Su primer vuelo fue en 2005 y se convirtió en uno de los proyectos de 
        construcción más complejos y de considerable ambición en la historia de la aviación actual.
    </pre>
    <br>
    <pre class="texto">
        Además, este modelo se destaca por su impresionante tamaño y su innovadora configuración interior, que permite transportar entre 500 y más de 
        800 pasajeros según la disposición elegida por cada aerolínea, su cabina espaciosa ofrece mayor comodidad, reducción significativa de ruido y 
        posibilidades únicas como suites privadas, áreas de descanso e incluso duchas en primera clase. Aunque su producción finalizó en 2021 debido 
        a cambios en las tendencias del mercado hacia aeronaves más eficientes y de menor tamaño, el A380 continúa siendo un símbolo de ingeniería 
        avanzada, lujo y grandeza dentro de la aviación comercial.
    </pre>

