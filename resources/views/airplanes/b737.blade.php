    <h2 class="h2Historia">Historia</h2>
    <pre class="texto" id="texto" >
        El Boeing 737 se considera uno de los tipos de aviones comerciales que tuvo más éxito, así como uno de los que se vendió como pan caliente en
        la historia de la aviación comercial, ya que fue destinado a las rutas de corta y media distancia y lo diseñó el fabricante estadounidense
        Boeing. Este reactor de un solo pasillo fue diseñado durante la década de los años sesenta con la intención de dar cabida a la necesidad de 
        una alternativa que ofreciera a las compañías aéreas una solución económica, eficiente y flexible a la ejecución de los vuelos regionales y
        nacionales con alta rotación de uso, gracias a sus proporciones compactas y a un rendimiento fiable, el 737 se posicionó rápidamente como 
        una pieza clave del transporte aéreo internacional.
    </pre>
    <br>
    <pre class="texto">
        A lo largo de los años, el modelo boeing 737 ha evolucionado a través de múltiples generaciones, incorporando mejoras en aerodinámica, motores 
        más eficientes y sistemas digitales avanzados en la cabina. Versiones modernas como la serie 737 Next Generation y la familia 737 max han optimizado 
        el consumo de combustible y reducido las emisiones, manteniendo su competitividad en el mercado global, es por ello que su versatilidad, bajo costo 
        operativo y facilidad de mantenimiento han permitido que miles de unidades estén en servicio alrededor del mundo, consolidándolo como uno de los 
        aviones más importantes y representativos de la aviación comercial actual.
    </pre>

