    <h2 class="h2Historia">Historia</h2>
    <pre class="texto" id="texto" >
        El Boeing 747 es uno de los aviones más icónicos en la historia de la aviación comercial, desarrollado por el fabricante estadounidense Boeing a 
        finales de la década de 1960 con el objetivo de revolucionar el transporte aéreo de larga distancia. Este avión de fuselaje ancho fue el primero 
        en introducir el concepto de “jumbo jet”, permitiendo transportar una cantidad de pasajeros nunca antes vista en un solo vuelo. Su diseño distintivo 
        con la joroba en la parte frontal superior se convirtió rápidamente en un símbolo mundial de innovación y grandeza en la industria aeronáutica.
    </pre>
    <br>
    <pre class="texto">
        El 747 realizó su primer vuelo en 1969 y entró en servicio comercial en 1970, transformando la manera en que las personas viajaban entre continentes. 
        Equipado con cuatro motores y una estructura robusta, fue durante décadas el avión de pasajeros más grande del mundo hasta la llegada del Airbus A380. 
        A lo largo de sus diferentes versiones, el 747 incorporó mejoras tecnológicas, mayor eficiencia y adaptaciones tanto para transporte de pasajeros como 
        de carga. Aunque la producción finalizó en 2023, el Boeing 747 permanece como una leyenda de los cielos y un referente histórico en la evolución del 
        transporte aéreo moderno.
    </pre>

